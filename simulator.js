let ctx = canv.getContext('2d')
let x = 20;
let y = -20;
let a = 0;
let step

let vel = 0;
let avel = 0;

let damping = 0.015;
let beta = 0.7;

let points = []

function draw(){
    ctx.clearRect(0,0,400,400)
    requestAnimationFrame(draw)

    // draw axes
    ctx.fillRect(200, 0, 1, 400)
    ctx.fillRect(0, 200, 400, 1)

    ctx.strokeStyle = 'red'
    drawPath(profile)

    ctx.strokeStyle = 'black'
    drawPath(points)

    // draw sys
    ctx.strokeStyle = 'black'
    drawRobot(x,y,a)
    ctx.strokeStyle = 'red'
    if(step){
    drawRobot(step[0], step[1], step[2])
    }
}

function drawPath(path){
    ctx.save()
    ctx.translate(200, 200)
    ctx.beginPath()
    if(path.length > 0){
    ctx.moveTo(path[0][0], path[0][1])
    path.forEach(element => {
        ctx.lineTo(element[0], element[1])
    });
    }
    ctx.stroke()
    ctx.closePath()
    ctx.restore()
}

function drawRobot(x,y,a){
    ctx.save()
    ctx.translate(x+200, y+200)
    ctx.rotate(a+Math.PI/2)
    ctx.strokeRect(-15.5, -15.5, 32, 32)
    ctx.beginPath()
    ctx.moveTo(0,0)
    ctx.lineTo(0, -64)

    ctx.stroke()
    ctx.closePath()
    ctx.restore()
}

let current = 0
let intv

let k1
let k2
let k3

function updateProfile(){

    step = profile[current]

    // update coefficients
    let k = 2 * damping * Math.sqrt(Math.pow(step[4],2) + beta * Math.pow(step[3], 2))
    k1 = k
    k3 = k
    k2 = beta * Math.abs(step[3])

    // console.log(`${k1} ${k2} ${k3}`)
    // console.log(`desired=${step[2]} actual=${a}`)
    // console.log(`errors: p=${Math.hypot((step[1]-y), (step[0]-x)).toFixed(2)} a=${((Math.abs(step[2]) > Math.PI) ? Math.abs(a-step[2]+Math.PI*2) : Math.abs(a-step[2])).toFixed(2)}`)
    vel = step[3] * Math.cos(step[2] - a) + k1 * (Math.cos(a) * (step[0] - x) - Math.sin(a) * (step[1] - y))
    avel = step[4] + k2 * Math.sign(step[3]) * (Math.sin(a) * (x-step[0]) - Math.cos(a) * (y-step[1])) + k3 * (step[2] - a)

    if(vel < 0) {
        beta = 0.1
    } else {
        beta = 0.7
    }
    // cap the angular velocity in case it wants to be a fidget spinner
    if(Math.abs(avel) > Math.PI*2){
        avel  = Math.sign(avel) * Math.PI*2
    }

    x += Math.cos(a) * vel * 0.05
    y += Math.sin(a) * vel * 0.05
    a += avel * 0.005

    points.push([x,y])

    current++;
    if(current >= profile.length){
        console.log("Profile completed")
        window.clearInterval(intv)
    }
}

window.onload = function(){
    intv = window.setInterval(updateProfile, 1000*0.05)
    draw()
}

